#include <stdio.h>
#include <unistd.h>

void	ft_putstr(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		write (1, &str[i], 1);
		i++;
	}
}

int	main(int ac, char **av)
{
	if (ac == 1)
	{
		ft_putstr(av[0]);
		ft_putstr("\n");
	}
	return (0);
}
